import {useLoaderData, Link, redirect} from 'react-router';
import type {
  LoaderFunctionArgs,
  ActionFunctionArgs,
} from '@shopify/remix-oxygen';

export async function loader({context, request}: LoaderFunctionArgs) {
  const {customerAccount} = context;
  const isLoggedIn = await customerAccount.isLoggedIn();

  if (isLoggedIn) {
    // Check if there's a returnTo parameter
    const url = new URL(request.url);
    const returnTo = url.searchParams.get('returnTo');

    if (returnTo) {
      return redirect(returnTo);
    }

    return redirect('/account');
  }

  return {};
}

export async function action({request, context}: ActionFunctionArgs) {
  const formData = await request.formData();

  const email = formData.get('email') as string;
  const password = formData.get('password') as string;

  if (!email || !password) {
    return {
      error: 'Email and password are required',
    };
  }

  try {
    // Shopify Customer Account API uses OAuth flow
    // For now, redirect to Shopify's customer login
    const url = new URL(request.url);
    const returnTo = url.searchParams.get('returnTo');

    let shopifyLoginUrl = `https://${context.env.PUBLIC_STORE_DOMAIN}/login`;

    // If there's a returnTo parameter, we'll need to handle it after Shopify login
    if (returnTo) {
      const returnUrl =
        url.origin + '/account/login?returnTo=' + encodeURIComponent(returnTo);
      shopifyLoginUrl += '?return_url=' + encodeURIComponent(returnUrl);
    }

    return redirect(shopifyLoginUrl);
  } catch (error) {
    console.error('Login error:', error);
    return {
      error: 'Login failed. Please try again.',
    };
  }
}

