export async function action({request}: ActionFunctionArgs) {
  const form = await request.formData();
  const name = String(form.get('name') || '');
  const email = String(form.get('email') || '');
  const subject = String(form.get('subject') || '');
  const message = String(form.get('message') || '');

  if (!name || !email || !subject || !message) {
    return json({error: 'All fields are required'}, {status: 400});
  }

  // Email validation
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!emailRegex.test(email)) {
    return json({error: 'Please enter a valid email address'}, {status: 400});
  }

  try {
    // Here you would typically send the email using a service like SendGrid, Mailgun, etc.
    // For now, we'll just simulate success
    console.log('Contact form submission:', {name, email, subject, message});
    
    return json({success: 'Thank you for your message! We\'ll get back to you soon.'});
  } catch (error) {
    return json({error: 'Failed to send message. Please try again.'}, {status: 500});
  }
}

