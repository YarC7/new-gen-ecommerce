import {useLoaderData, Link, redirect} from 'react-router';
import type {
  LoaderFunctionArgs,
  ActionFunctionArgs,
} from '@shopify/remix-oxygen';

export async function loader({context, request}: LoaderFunctionArgs) {
  const {customerAccount} = context;
  const isLoggedIn = await customerAccount.isLoggedIn();

  if (isLoggedIn) {
    // Check if there's a returnTo parameter
    const url = new URL(request.url);
    const returnTo = url.searchParams.get('returnTo');

    if (returnTo) {
      return redirect(returnTo);
    }

    return redirect('/account');
  }

  return {};
}

export async function action({request, context}: ActionFunctionArgs) {
  const formData = await request.formData();

  const email = formData.get('email') as string;
  const password = formData.get('password') as string;
  const firstName = formData.get('firstName') as string;
  const lastName = formData.get('lastName') as string;

  if (!email || !password || !firstName || !lastName) {
    return {
      error: 'All fields are required',
    };
  }

  try {
    // Get the returnTo parameter to preserve it through the registration flow
    const url = new URL(request.url);
    const returnTo = url.searchParams.get('returnTo');

    // For now, redirect to Shopify's customer registration page
    // In a real implementation, you'd handle customer creation via Shopify Admin API
    let shopifyRegisterUrl = `https://${context.env.PUBLIC_STORE_DOMAIN}/account/register`;

    // If there's a returnTo parameter, preserve it through the registration flow
    if (returnTo) {
      const returnUrl =
        url.origin + '/register?returnTo=' + encodeURIComponent(returnTo);
      shopifyRegisterUrl += '?return_url=' + encodeURIComponent(returnUrl);
    }

    return redirect(shopifyRegisterUrl);
  } catch (error) {
    console.error('Registration error:', error);
    return {
      error: 'Registration failed. Please try again.',
    };
  }
}

